int __attribute((section(".ARM.__at_0x0001000"))) app_main(void) {
    
    // LED1 on
    NRF_P0->OUTCLR                     = (0x00000001 << 13);    // LED 1

    // loop
    while(1) {

        // wait for event
        __SEV(); // set event
        __WFE(); // wait for event
        __WFE(); // wait for event
    }
}

